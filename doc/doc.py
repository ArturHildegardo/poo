'''
    DESCRIPTION:

    função soma faz fazer a soma de duas variaveis do tipo inteiro, passado por argumentos
    funçãi mut envia o resutado de uma mutiplicação de inteiros passado por dois argumentos
'''

def soma(val1, val2):
    '''
    :param val1: int
        valor do tipo inteiro
    :param val2: int
        valor do tipo inteiro

    :raise:
        So aceita valor do tipo inteiro

    :return:
        vai retorna a soma dos valores passados como argumentos
    '''
    return val1 + val2

def mut(m1, m2):
    '''
    :param val1: int
        valor do tipo inteiro
    :param val2: int
        valor do tipo inteiro

    :raise:
        So aceita valor do tipo inteiro

    :return:
        vai retorna a soma dos valores passados como argumentos
    '''
    return m1 * m2